fun main() {

    //Arithmetic Operators
    var x = 20
    var y = 5
    println( x + y )
    println( x * y )
    println( x / y )
    println( x - y )
    println( x % y ) //Modulus

    //Comparison Operators
    var a = 67
    var b = 30
    println(a < b)
    println(a > b)
    println(a <= b)
    println(a >= b)
    println(a == b)
    println(a != b)

    //Assignment Operators
    var number = 90
    println(number)

    number += 3
    println(number)

    number -= 3
    println(number)

    //Logical operators - &&(and), ||(or), !(not)
     var num = 10
     println(num >67 && num <20)
     println(num >67 || num <20)
     println(!(num >67 || num <20))
}
